# Entradas

metros_cuadrados = int(input('Ingrese la superficie a pintar en metros cudrados: '))
monto_x_mtc = 1000

# Proceso

mano_de_obra = monto_x_mtc * metros_cuadrados

# Salida

print(f'El costo de mano de obra para pintar \nuna pared de {metros_cuadrados} '
      f'metros cuadrados es : {mano_de_obra}$')

